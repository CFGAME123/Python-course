"""
    se utiliza para establecer una relacion entre 2 valores
    comparando los valores entre si y realiza un resultado de verdado o falsedad (bool)
"""

"""
    > mayor que
    < menor que
    >= mayor o igual que
    <= menor o igual que
    != diferente
    == igual
"""

resultado = 10<20
print(resultado)
resultado = 10>20
print(resultado)
resultado = 10 == 20
print(resultado)
resultado = 10 != 20
print(resultado)
resultado = 10 <= 20
print(resultado)
resultado = 10 >= 20
print(resultado)

#se pueden realizar operaciones relacionales con variables
a = 10
b = 12
resultado = a == b
print(resultado)