"""
    me sirven para comparar 2 valores y me dan un valor logico y dependiendo de que resultado optenga da otro resultado o no da nada
"""

numero = float(input("escribe un numero: "))

if numero >0: #poner 2 puntos para terminar la condicion
    print("es positivo :D")
    
elif numero == 0: #sirve para tener nuevas condiciones
    print("es cero 7w7")
    
else: #viene ser lo contrario al if en caso de que no se cumpla la condicion
    print("es negativo D:<")