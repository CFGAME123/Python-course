#operadores aritmeticos

suma = 10 + 3 #usando el simbolo " + " se hace la suma
print(suma)

resta = 10 - 3 #usando el simbolo " - " se hace la resta
print(resta)

multiplicacion = 10*3 #usando el simbolo " * " se hace la multiplicacion
print(multiplicacion)

divicion = 10/3 #usando el simbolo " / " se hace la divicion
print(divicion)

divicion_redondeada = 10//3 #usando el simbolo " // " se hace una divicion redondeada hacia abajo o entera
print(divicion_redondeada)

modulo = 10%3 #usando el simbolo " # " se hace la operacion de modulo la cual divide los 2 numeros y retorna el residuo
print(modulo)

exponenciacion = 10**2 #realiza la potencia de los operando 
print(exponenciacion)

